/// A single legal document, fully data-driven so Privacy + Terms (and Android)
/// stay byte-identical. `sectionTitles` doubles as the `LegalTOCCard` items and
/// the `LegalSection` headings.
struct LegalDocModel {
    let lastUpdated: String
    let version: String
    let sections: [LegalDocSection]
    let contactEmail: String
    let contactLabel: String

    var sectionTitles: [String] { sections.map(\.title) }
}

struct LegalDocSection {
    let title: String
    let blocks: [LegalBlock]
}

enum LegalBlock {
    /// Plain running paragraph.
    case paragraph(String)
    /// Paragraph with inline bold defined terms (`<DT>`).
    case rich([LegalRun])
    /// Bold sub-heading (`<H3>`).
    case subheading(String)
    /// Dotted bullet list.
    case bullets([String])
}

/// One inline run of paragraph text. `bold` marks a defined term (`<DT>`).
struct LegalRun {
    let text: String
    let bold: Bool

    /// Plain run.
    static func t(_ text: String) -> LegalRun { LegalRun(text: text, bold: false) }
    /// Bold defined-term run.
    static func b(_ text: String) -> LegalRun { LegalRun(text: text, bold: true) }
}

